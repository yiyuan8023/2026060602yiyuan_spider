def create_data_mapping(title_index, values):
    """
    将标题索引和对应的值关联起来
    """
    result = {}
    for title, index in title_index.items():
        if index < len(values):
            result[title] = values[index]
        else:
            result[title] = None  # 如果索引超出范围，设为None
    return result


